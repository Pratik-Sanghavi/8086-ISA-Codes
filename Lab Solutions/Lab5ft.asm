.model tiny
.486
.data
FNAME DB 'FRESH',0
HANDLE DW ?
MAXINP DB 100
ACTINP DB ?
INPUT DB 100 DUP('$')
DSTRING DB 'Please enter personal information',0Dh,0Ah,'$'
.code
.startup
MOV AH,3Ch
MOV CL,20h
LEA DX,FNAME
INT 21h
MOV HANDLE,AX

LEA DX,DSTRING
MOV AH,09h
INT 21h

LEA DX,MAXINP
MOV AH,0Ah
INT 21h

LEA DI,INPUT
MOV AL,'$'
MOV CX,100
MOV DX,100
CLD
REPNE SCASB
SUB DX,CX
MOV CX,DX
DEC CX

MOV AH,40h
MOV BX,HANDLE
LEA DX,INPUT
INT 21h
.exit
end